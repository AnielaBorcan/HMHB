<?php
	function mess(&$message)
	{
		$len = sizeof($message);
		if ($len > 30)
			$len = 30;
		for ($i=0; $i < $len; $i++)
		{
			echo  $message[$i] .PHP_EOL ;
		} 					 
			
		echo "... ";
		echo PHP_EOL;
	}
